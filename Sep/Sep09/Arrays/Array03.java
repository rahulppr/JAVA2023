package Sep.Sep09.Arrays;


public class Array03 {
    public static void main(String[] args) {
        String [] fruits = new String[2];
        fruits[0] = "Mango";
        fruits[1] = "Apple";

        for (int i = 0; i < fruits.length ; i++) {
            System.out.println(fruits[i]);
        }
    }
}

